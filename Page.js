//داله لتحقق من ان كل الاساله تم الاجابة عنها

function validateForm() {
    const questions = document.querySelectorAll('input[type="radio"]');
    const questionCount = {};

    // عد عدد الأزرار لكل سؤال
    questions.forEach((radio) => {
        const questionName = radio.name;
        if (!questionCount[questionName]) {
            questionCount[questionName] = 0;
        }
        if (radio.checked) {
            questionCount[questionName] += 1;
        }
    });
    
    // تحقق مما إذا كان كل سؤال لديه اختيار
    for (let question in questionCount) {
        if (questionCount[question] === 0) {
            alert(question+'يرجى اختيار إجابة لكل سؤال.');
            return false; // يمنع الإرسال
        }
    }

    return true; // يسمح بالإرسال
}