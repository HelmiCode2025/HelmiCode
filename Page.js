function createQuestion(question, answers) {
    const container = document.getElementById('question-container');
   
    // إنشاء div جديد للسؤال
    const questionDiv = document.createElement('div');
    questionDiv.classList.add('question'); // إضافة فئة CSS للسؤال (اختياري)
    questionDiv.className="container";

    // إنشاء عنوان السؤال
    const questionTitle = document.createElement('h2');
    questionTitle.textContent = question;
    questionDiv.appendChild(questionTitle);

    // إنشاء خيارات الإجابة
    answers.forEach((answer, index) => {
        const radio = document.createElement('input');
        const label = document.createElement('label');
        
        radio.type = 'radio';
        radio.name = `q_${container.children.length+1}`; // استخدام اسم فريد لكل مجموعة من الخيارات
        radio.value = index+1;
        radio.id = `answer_${container.children.length+1}_${index}`; // إضافة معرف فريد لكل إدخال
         radio.setAttribute('style','direction:ltr')

        label.htmlFor = radio.id; // ربط التسمية مع الإدخال
         label.setAttribute('style','text-align: right')
        label.appendChild(radio);
        label.appendChild(document.createTextNode(answer));
        questionDiv.appendChild(label);
    });

    // إضافة div السؤال إلى الحاوية الرئيسية
    container.appendChild(questionDiv);
}
